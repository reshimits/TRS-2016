                    <h3>Things to Consider</h3>
                    <p class="lead">1. What is the original halogen bulb type that the vehicle used for its low beam/main headlight?</p>
                    <p>The answer here determines the spec harness you will need. ie 9006, H4, etc consequently, it also determines whether you will need a low beam or bixenon harness, since that's correlated with the spec on the input. For example, a 9006 harness will be a low beam model, and H4 would be a bixenon model.</p>

                    <hr class="aligncenter">

                    <p class="lead">2. Does the car have Can-Bus system?</p>
                    <p>Can-bus means that the ECU on the vehicle communicates with various components around the car to check and make sure that all of these systems are working properly. It's an important consideration here because if the ECU senses that the resistance across the headlight circuit is different than it's programmed to accept as the norm- that's when you run into problems. Anything from a simple "lamp out" warning on the dash to a constant flicker to the headlights may result.</p>
                    <p><span class="text-weightBold">The fix:</span> Typically you just need to add a bit of resistance to the circuit in order to trick the car into thinking that the original headlight bulbs are still plugged in. normal relay harnesses that only plug into one side of the car won't cut it, since they leave the other factory output completely hanging- so an entirely different harness that utilizes both outputs will be required. Morimoto standalone can-bus harnesses are the suggested recipe for success in most European applications, whereas the Mopar harness series is suggested for most American vehicles with CanBus issues.</p>
