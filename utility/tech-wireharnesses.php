                    <p class="lead">3. Did the vehicle come with factory HID's?</p>
                    <p>If you're upgrading or changing the ballasts on a vehicle that came factory equipped with HID headlights, chances are you won't need any relay harnesses at all. If you're adding another set of hid ballasts to work alongside the factory hid's though ( when converting to quad projectors for example) then yes- you want a low beam relay harness. the input spec on the harness will not matter, as you will have to tap into the -/+ input that powers one of the current ballasts anyways.</p>

                    <hr class="aligncenter">

                    <p class="lead">4. Does the car have a Daytime Running Light (DRL)?</p>
                    <p><span class="text-weightBold">If the car has a daytime running light on the high beam:</span> Don't worry about it, it has no effect.</p>
                    <p><span class="text-weightBold">If the car has a daytime running light on the low beam and does not have a can-bus system:</span> Use a normal relay harness. The low beam HID will become your DRL.</p>
                    <p><span class="text-weightBold">If the car has a low voltage daytime running light on the low beam and a can-bus system:</span> This poses a problem because the can-bus harnesses won't provide adequate power to the ballasts and the regular relay harness will trigger a lamp-out warning or flickering. The solution is to disable the DRL preferably. If that's not technically or legally possible (e.g. if you reside in Canada where all vehicles must have them) then you have to go for the can-bus harnesses and remember to turn your low beams on every time you drive. This will deliver full (instead of half) voltage to the headlight circuit and provide the power that the ballasts need to run without flickering.</p>

                    <hr class="aligncenter">

                    <p class="lead">5. Are you doing a quad projector retrofit?</p>
                    <p><span class="text-weightBold">With a single projector retrofit:</span> There are no more considerations than the ones mentioned above.</p>
                    <p><span class="text-weightBold">With a quad projector retrofit:</span> The chances are that your original headlights were setup with a separate low and high beam bulb. In question one above, you probably selected a low beam harness, like a 9006, H7, or H11. Here, you will need two of those harnesses, since each one will have two outputs. In the end you have outputs for four ballasts triggered off of the two original low beam outputs from the car.</p>
                    <p><span class="text-weightBold">If only one set of the projectors in your quad retrofit are bi-xenons:</span> Then just plug the remaining set of outputs from your car (that used to plug into the halogen high beam bulbs) into the projector solenoids.</p>
                    <p><span class="text-weightBold">If both sets of projectors in your quad retro are bi-xenons:</span> Then you will also need a set of high beam splitters. These will split the signal from the factory high beam plugs into two bixenon projector solenoids per side.</p>
