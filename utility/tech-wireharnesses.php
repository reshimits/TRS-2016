                    <p><span class="text-weightBold">High Beam Splitter:</span><br>
                    These split the signal from the high beam output into two different high beam sources. Whether you will be running a bixenon projector alongside an existing halogen high beam or two bixenon projectors per headlight, these will take care of the required connections.</p>
                    <p><span class="text-weightBold">Ground switched vs. Positive switched harnesses:</span><br>
                    Positively switched headlights are much more common, but a lot of applications nowadays run what is known as ground switched headlights. (Toyotas, Subaru's, Volkswagon's for example) The difference here is in pin that becomes active to complete the circuit, and thus trigger the harness to activate the headlights. In the more common positively switched setups, the ground is constant and the positive pin switches on and off. In ground switched setups, the positive pin stays hot, and the system is grounded when the headlight switch is turned on inside the cabin.</p>
                    <p><span class="text-weightBold">When can-bus harnesses aren't enough:</span><br>
                    The best way to battle an incessant flicker problem is installing a 4700uF capacitor in between the positive and ground wireâ€™s on the input line to a normal relay harness. Despite any pulsing signals from the factory harness, the inline capacitor will store a charge and release it smoothly into the relays for uninterrupted operation.</p>
