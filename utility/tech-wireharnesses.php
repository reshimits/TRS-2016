                    <h3>Further Reading</h3>
                    <p><span class="text-weightBold">Low Beam Relay Harness:</span><br>
                    Your factory socket that originally connected to the halogen low beam bulb now switches on a relay. The relay is a switch that draws power from the car battery and sends it out to the ballasts.</p>
                    <p><span class="text-weightBold">Bi-xenon Relay Harness:</span><br>
                    A bi-xenon harness is a bit more complicated compared to a standard low beam wire harness. A typical dual-filament halogen bulb has 3 pins: one for the low beam, one for the high beam, and a ground. In low beam or high beam mode; only that pin is receiving a signal from the factory harness. The low beam mode is simple, but in a bi-xenon retrofit, you need the low and high beams powered on at the same time. Otherwise, when you hit the high beams, the projectors will turn to high beam mode but the ballasts will turn off - leaving you with nothing but darkness. A properly configured harness will correct for this problem. There are two ways a bi-xenon harness can be setup to do so.</p>
                    <p><span class="text-weightBold">With a diode:</span><br>
                    The diode will be installed between the low and high beam input wires on the harness. A diode is a like a one-way road that lets current flow from the high beam lead into the low beam lead - which not only keeps the relays powering the ballasts but passes through to the bixenon solenoids to activate the hi beam. (For more on how bixenon projectors work, <a href="tech-projectors.php">read here</a>).</p>
                    <p><span class="text-weightBold">With a control box:</span><br>
                    This will control the function of the low and high beams via it's own specialized/smart circuitry. When the control box reads "low beam input pin active" it will only output power to the ballasts. When the control box reads "high beam input pin active" it knows to send current to the ballasts and the bi-xenon solenoids at the same time. These are more complicated and less serviceable, but often times more universally applicable with positive and ground switched circuits.</p>
                    <p><span class="text-weightBold">Can-Bus Harness:</span><br>
                    Simply put, our standalone can-bus harnesses plug draw power directly from the factory outputs and supplies it to the ballasts. They incorporate a resistor to fool the ECU into thinking the original halogen bulbs are still installed (preventing error codes) and also a capacitor, to provide a short burst of current to the ballasts on ignition. Once lit, the ballasts rely on the normal ~12v to operate.</p>
