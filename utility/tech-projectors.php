                    <h3>Further Reading</h3>
                    <p><span class="text-weightBold">Using a bi-xenon projector in headlights with separate halogen high beams:</span><br>
                    Even if you don't need a bi-xenon, you can still use one. When you retrofit a bi-xenon projector into a headlight housing with a separate halogen high beam - you will have the ability to easily wire it up so the headlights have dual high beams. The solenoid from the projector can be tied into the wires that powers the halogen high beam bulb to activate the bi-xenon high beam and the halogen high beam at the same time using our splitters.</p>
                    <p><span class="text-weightBold">How bi-xenon projectors work:</span><br>
                    Bi-xenon means each projector has both a low and high beam pattern. Here, the light cutoff shield inside the projector is on a hinge. When powered, the bi-xenon solenoid pulls or pushes this flap/shield down to expose more light through the lens - thus creating the high beam pattern. When the high beam is deactivated, the cutoff shield retracts to its normal low beam position. If you ever researched HID headlights, you probably know its bad to flash an HID bulb on and off repeatedly like you would when using a high beam on a dark road. Bi-xenon projector systems are not subject to this problem because when toggling between low and high beams - the bulb is constantly lit, does not move, get any brighter, etc. The projector creates the high beam, not the bulb.</p>
