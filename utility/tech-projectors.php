                <div class="col-xs-12">
                    <p><span class="text-weightBold">Why are some projectors so expensive? </span><br>
                    Is price or performance more of a concern? When it comes to OEM projectors, simple corollary here is that performance = demand = price. It's hard to find a pair of OEM Honda S2000 projectors because they're in such high demand as a result of their superior low beam output. If you do manage to get your hands on pair, expect to pay $300- 400 for them. OEM S2000 projectors will easily go for 3x more than a pair of undesirable Bosch/E46 projectors would. Other expensive projectors include Lexus SC430, LS430, 460, and RX350 for their rarity, and intense uniform and wide beam patterns, Acura TSX's and TL's for their good output in stock form. Here, it's mostly about exclusivity and perfection. Aftermarket projectors usually pack more "bang for the buck" like the FX-R or Morimoto Minis. They cost much less because they're readily available, but realistically still out-perform many of the more common OEM projectors.</p>

                    <p><span class="text-weightBold">Fresnel vs Clear lenses: </span><br>
                    Most projectors come from the factory with fresnel or cloudy optical lenses. These "detuned" lenses never let the projectors perform to their full potential. They put a damper on the degree of sharpness, color, and light intensity produced by the projectors. Some projectors come stock with better quality optically clear lenses, but even these can be improved upon (ie TSX). Think of a projector just like any other original part on your car, of course there will always be an aftermarket performance/aesthetic upgrade available (ie lens swap). A clear lens upgrade is like replacing your intake and exhaust with a high flow setup, but for headlights!</p>

                    <p><span class="text-weightBold">Sources for Projectors:</span><br>
                    There are some important things to look for in buying a set of projectors. We cannot stress how important it is to buy your projectors from a reputable source. Too often we get inquiries from individuals who come to us looking for spare projector parts because the set they thought they were getting a good deal on didn't come complete or had ruined parts. Make sure the bulb clips, cutoff shields, solenoid wiring are included. Make sure your lenses aren't cracked, and make sure the reflector bowls aren't tarnished, and your getting a true left/right pair. For aftermarket projectors like FX-R's or Mini's - make sure you're getting the absolute latest version and that it's not a cheaper/generic knock off. Aftermarket projectors often need additional tuning after they're manufactured to ensure their beams are corrected for focus/sharpness, etc. Buy from TRS and you don't have to worry about any of the above.</p>
