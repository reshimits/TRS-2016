                    <p><span class="text-weightBold">Bulb size:</span><br>
                    HID projectors are designed for use with D1S, D2S, or D4S bulbs. The overwhelming majority of projectors use D2S style bulbs. The only HID projector that doesn't use a D-series bulb is the Morimoto Mini H1, which is designed for use with compact H1 HID bulbs. You must use the type of bulb that the projector was designed for. No, you cannot simply stick the bulb from your old HID kit in the back of a projector and expect it to fit or work well. If you're using a bi-xenon projector, you do not need bi-xenon specific bulbs.</p>
                </div>
                <div class="col-xs-12 col-sm-6">
                    <p><span class="text-weightBold">LHT vs. RHT: </span><br>
                    If you live in a country where the standard is LHD (left hand drive) then you need to use a projector with a beam pattern compatible with your traffic pattern. The step in the beam pattern will go up and to the right. In North America where LHD is the standard, the step in the beam pattern goes up and to the right. In countries like Australia, the UK, and India where the standard is RHD, the beam pattern from the headlights will be lower on the right, higher on the left. What's the point? On the side of oncoming traffic, your beam will be lower so you don't blind them. On the side directly in front of your car, your headlight beam will shine further down the road.</p>
