                    <h3>Things to Consider</h3>
                    <p class="lead">1. Do you need a bi-xenon or a low beam?</p>
                    <p>This choice is completely based on how your headlights are set up now. Do they use a single bulb with two filaments to produce the low and high beams? Or, do they use one bulb for the low beam, and another bulb for the high beam? If the vehicle uses two bulbs, then you can use a low beam or a bi-xenon projector. If the vehicle uses a single "dual filament" bulb, then you must use a bi-xenon to retain the full functionality of your low and high beams.</p>

                    <p><span class="text-weightBold">If your vehicle uses the following bulb type, you can use a low beam or bi-xenon:</span> <br>
                    — H1 H3 H7 H8 / H9 / H11 H10 / 9145 9005 / HB3 9006 / HB2</p>

                    <p><span class="text-weightBold">If your vehicle uses the following bulb type, you need to use a bi-xenon:</span> <br>
                    — 9003 / H4 / HB2 9004 / HB1 9007 HB2 9008 / H13</p>
