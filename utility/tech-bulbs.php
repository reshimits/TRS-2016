                        <a name="sizeguide"></a>
                        <p class="lead">Quick Size Reference Guide:</p>
                    </div>
                    <div class="col-xs-12">
                        <p><span class="text-weightBold">D2S:</span> Used for projector-based HID headlight setups. The most common type of HID bulb.(S stands for Shielded, as in the "cutoff shield" inside the projector).</p>
                        <p><span class="text-weightBold">D2R:</span> Used for older reflector-based HID headlight setups.(R stands for reflected).</p>
                        <p><span class="text-weightBold">D2C:</span> "Universal" HID bulb that can be used in reflector or projector based HID headlights. Only produced by aftermarket manufactures.</p>
                        <p><span class="text-weightBold">D4S:</span> An entirely new generation of more environmentally safe HID bulbs. These bulbs are 100% mercury free, and are marginally brighter than their predecessors. They require special ballasts designated for use with D4 style bulbs. Designated as "XenEco" bulbs by their manufacture; Philips. According to our customer demand for OE replacements, reliability isn't up to par with D2 systems.</p>
                        <p><span class="text-weightBold">D1S/D1R:</span> Similar to the D2 series bulbs in that "S" denotes for projector use, and "R" denotes for reflector use. The main difference compared to the "D2" series is that the igniter is integrated into the base of the bulb. The igniter is built into the bulb here instead of being part of the ballast system. Newer than D2S/D2R, but not as popular since most higher performance projectors are for D2 bases.</p>
                        <p><span class="text-weightBold">D3S:</span> Similar to the D1 series, but with eco-friendly (mercury free) capsules like D4's.</p>
                        <p><span class="text-weightBold">H1:</span> The most popular non D series bulb, uses a D2 capsule built on a compact base and compatible with the Morimoto Mini H1 Bi-xenon projectors.</p>
                        <p><span class="text-weightBold">All Other Sizes: H1, H3, H4, H7, H8, H9, H10, H11, H13, 9003, 9004, 9006, 9007 etc.</span> These are the most common sizes for original equipment halogen headlight bulb sizes, which often have HID versions available through aftermarket manufactures. You will never find a set of H7, H4, etc HID bulbs that are truly made by Philips, Osram, or any other OEM manufacture. (Despite the fact that they're fairly easy to find, those are fake).</p>
                    </div>    
                </div>
                </div> 
                <div class="col-xs-12 col-sm-6">
                <div class="row">
                    <div class="col-xs-12">
                        <a name="kelvinreference"></a>
                        <p class="lead">Kelvin Reference Guide:</p>
                    </div>
                    <div class="col-xs-12">
                        <p><span class="text-weightBold">3000K:</span> Has a golden yellow output perfect for use in fog lights. The yellow light penetrates through snow, rain, and fog more effectively than white or blue light, making it more ideal for bad weather applications.</p>
                        <p><span class="text-weightBold">4300K:</span> Has a slightly off-white output similar to that of sunlight. All cars that come equipped with HID headlights from the factory use 4300K bulbs. If you want the most usable light, 4300K is the kelvin rating for you.</p>
                        <p><span class="text-weightBold">5000K:</span> A pure white output, no tinge of yellow, and no tinge of blue.</p>
                        <p><span class="text-weightBold">6000K+:</span> As you move into the higher kelvin ratings, your light output will appear more bluish-purple in hue, but the bulbs won't be physically as bright.</p>
                        <p><span class="text-weightBold">D1S/D1R:</span> Similar to the D2 series bulbs in that "S" denotes for projector use, and "R" denotes for reflector use. The main difference compared to the "D2" series is that the igniter is integrated into the base of the bulb. The igniter is built into the bulb here instead of being part of the ballast system. Newer than D2S/D2R, but not as popular since most higher performance projectors are for D2 bases.</p>
                        <p><span class="text-weightBold">Color-Shifting:</span> As HID bulbs age, they go through what is known as "color-shifting". When the bulbs are brand new, the output is whitish-yellow and as physically bright as it will ever be. As the bulbs age, their output turns more pure white/bluish and also starts to dim a bit. That's why cars with factory 4300K bulbs that are 5+ years old look like they're running 5000K or higher bulbs. This is due to the deformation of the electrodes that occurs as time goes on. To understand this, one must understand that near the electrodes, a region of plasma glows deep blue. As the electrodes are deformed, this region grows larger and contributes more to the color of the bulb. Bulbs that resist color-shift have modified electrodes to prevent this deformation (e.g. Philips 85122+). Another tactic is to make it so that the blue area around the electrodes is not in an area that the projector can "see". This helps to minimize its effect as well.</p>
                        <p><span class="text-weightBold">Color Flicker:</span> Choosing a high-kelvin bulb doesn't guarantee you'll get the exotic color flicker effect that everybody lusts after. The effect is produced entirely by the projector and lens combination.</p>
