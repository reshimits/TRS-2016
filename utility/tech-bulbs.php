                    <h3>Things to Consider</h3>
                    <p><span class="text-weightBold">1. Size/Fitment:</span> The first and foremost item of importance is the size of the bulb. It doesn't matter if you spent $250 on the best set of bulbs known to mankind; if they won't properly fit the housing/fixture that they're intended for, your light output will be awful. This goes for any install, whether you're considering an OEM HID projector based retrofit requiring D2S, or an upgrade to the factory halogen projectors designed for H1, H7, etc. When the bulb is fully seated in the back of your projector, the distance and centering of the arc-capsule relative to the bulb base is very important. Projector optics are so sensitive that if the bulb doesn't fit well, these points will be off, and your output will suffer. Find out what size you need first before looking into your options. <a href="#sizeguide">Size Guide</a></p>

                    <p><span class="text-weightBold">2. Precision:</span> The alignment of the arc capsule is crucial in achieving good light output. If the capsule is out of place in the reflector, even if it's off to one side or tilted down by a only a millimeter - the imperfection will be magnified and projected on the road through the lens. Buying a set of bulbs built to a set of higher standards will pay off with a tighter beam pattern, reducing otherwise hotspots, and producing a cleaner cutoff line. Make sure the bulb glass that rises from the base is surrounded by some sort of metal framework or tight-fitting ceramic sleeve to ensure the best possible beam pattern. Avoid bulbs constructed with a plastic-only base.</p>

                    <p><span class="text-weightBold">3. Halide salts:</span> The blend of salts in the capsule that compose the precise formula to achieve their Kelvin rating (such as 4300K) is what sets them apart. The main salts in 4300K bulbs are Scandium Iodide and Sodium Iodide, Dysprosium Iodide is also added to some bulbs. While in higher Kelvin bulbs, more Indium Iodide is used. This is what gives the salts in those bulbs their red color. It also is really expensive and hazardous to use, generally increasing their price. Less salts = less light and higher operating temperatures. When looking at the bulb (unlit) you should see some deposits in the capsule/bubble - those are the halide salts and they should be aplenty.</p>

                    <p><span class="text-weightBold">4. UV Output:</span> The outer glass shell on the bulb is there to filter harmful UV rays from the capsule, so it should be produced with that in mind. Materials like quartz are often used since they're more apt at softening UV output relative to other lesser expensive options. Like a person out in the sun, UV can damage/tarnish the interior of your headlight housings and projectors after prolonged exposure. Beware of this and shop for bulbs built with glass that offers some level of filtration.</p>

                    <p><span class="text-weightBold">5. Kelvin:</span> We commonly get customers looking for high kelvin [ie 8000K] bulbs because they want their headlights to be "brighter". There is a huge misconception here. As the Kelvin rating of a bulb increases, its Lumen rating decreases. Simply put: 4300K bulbs will put more usable light on the road compared to 6000K, 8000K, 15,000K bulbs. TRS does not offer bulbs with Kelvins higher than 6000K because A) We don't cater to obnoxious "enthusiasts" who want their headlights to look like a Christmas tree, and B) it starts to defeat one of the main purposes of the headlight upgrade: more light output. <a href="#kelvinreference">Kelvin Reference Guide</a></p>

                    <p><span class="text-weightBold">6. Wattage:</span> While it's not the end of the day if you run a 55w bulb with a 35w ballast or a 35w bulb with a 55w ballast, it is ideal to couple the two ratings. A 35w bulb ran at 55w will burn at a reduced Kelvin rating relative to its supposed measure. The lifespan may also be reduced by a marginal amount. A 55w bulb on a 35w ballast may last longer because of the beefed up electrodes, but may not always strike on as reliably.</p>

                    <p><span class="text-weightBold">7. Low beam or Bi-xenon Projectors:</span> When choosing a bulb from TRS, it does not matter whether you are using low beam or bi-xenon projectors. Unlike halogen headlights, with bi-xenons, nothing happens to the bulb when the high beams are activated. It does not get any brighter or shift positions. Simply put: you don't need "low beam" or "bi-xenon" specific bulbs. Our S2K-R low beam projectors and FX-R bi-xenons both use "D2S" bulbs.</p>

                    <p><span class="text-weightBold">8. Warranty:</span> Like any other bulbs on your car or in your house, HID bulbs will eventually burn out. When purchasing anything other than a set of $10 cheapo bulbs from eBay, make sure you're buying from a manufacture or distributor that offers a decent warranty and replacement policy so that in case something does happen to the bulb, you'll be covered quickly with a replacement.</p>

                    <p><span class="text-weightBold">9. Replacement policy:</span> Remember that bulbs color shift over time, so if your bulb goes bad a year after you bought it and the seller replaces it, it will not match the other one. Most reputable companies will replace both bulbs with new units should one go bad in a certain period of time. You should also be sure that you won't have to return the defective bulbs before any replacements are issued, since as soon as your headlight burns out, the clock starts ticking before you're issued a citation from the police.</p>

                    <p><span class="text-weightBold">10. Authenticity:</span> If you're considering forking out the big bucks for a set of more expensive OEM bulbs such as Philips 85122's or Osram CBI's - you can never be too careful checking the sellers reputation. Within the last year, we've seen more and more vendors advertising fake Philips bulbs especially. These bulbs are typically sold around the same average price or just under compared to the genuine ones, but don't offer nearly the same level of performance and quality. We estimate that more than half of all OE brand HID bulb sales on sites like eBay or Amazon are not real, so if the price looks too good to be true- it probably is. Buyer Beware!</p>
