                    <h3>Background</h3>
                    <h5 class="text-gray1">HID bulbs have a capsule filled with a mixture of noble gasses and metal halide salts. Unlike a halogen bulb that relies on a glowing tungsten filament to produce light, xenon bulbs work differently. When the ballast fires, it produces around 25K Volts; this is required to start the arc across the two electrodes inside the capsule of the HID bulb. At first, the added intensity and blue color you see is from a type of emission around the two electrodes. In a short time, the temperature in the arc chamber rises, vaporizing the salts. These vaporized salts are then ionized by the arc into a plasma. This plasma is what emits light. It also serves to allow the required arc voltage to drop to around 84V, which is easy for the ballast to maintain - making them very efficient.</h5>
                    <p>Typically, HID bulbs have a very long lifespan of around 2500+ hours, compared that to a standard halogen bulb, which generally only lasts around 300 hours. Some early High Intensity Discharge bulbs ran DC current, but they suffered from short bulb life. This is because in DC, one electrode is constantly bombarded by high speed positive ions. This results in a noticeable shortening and deforming of the electrode. Over time, this erodes away the electrode to the point that it fails. By running AC, the high-speed ions, hit both electrodes, essentially doubling the life of the bulb. Another benefit is that the electrodes, run a little cooler using AC because the anode and cathode change about 400X per second compared to DC where one is always anode and the other always cathode (shared heat over two electrodes versus just one).</p>
