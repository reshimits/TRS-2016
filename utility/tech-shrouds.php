                    <h3>Things to Consider</h3>
                    <p class="lead">1. Projectors:</p>
                    <p>What kind are you going to use? Some shrouds won't fit some projectors no matter how you trim them, so it's important to consider the compatibility here. Most of our shroud and projector options are cross compatible, but if you think you're going to cover a TL bi-xenon with an Mini Gatling Gun, you'll be in for it. Of course, pick a shroud that will cover a large enough surface area that it will cover the body of your projector and then some.</p>

                    <hr class="aligncenter">

                    <p class="lead">2. Shape:</p>
                    <p>Is the shape of the reflector housing that you just retrofitted a complete circle? A semi-circle, an oval? Typically, if you match the outside shape of the shroud with the opening in your headlights, the completed retrofit will look more like it was meant to be in the first place versus something "thrown together". (ie, if the reflector is a big open full-circle, pick a shroud that's larger and circular) For example, the Mustang below has a reflector dish with a completely circular shape, so a circular shroud would be perfect. The Jeep on the other hand has a reflector with a semi-circular shaped reflector, so a semi-circular shroud is ideal.</p>
                    <p><span class="text-weightBold">Circular:</span> Apollo, Bullet, G37-R, Gatling Gun, Gatling Gun 2.0, Gti-R, MC-R, Orbit</p>
                    <p><span class="text-weightBold">Semi-Circle (one flat edge):</span> E46-R, E46-R Extended, Iris</p>
                    <p><span class="text-weightBold">Oval:</span> E55-R, Ocular</p>
                    <p><span class="text-weightBold">Other:</span> Panamera</p>

                    <hr class="aligncenter">

                    <p class="lead">3. Depth:</p>
                    <p>Consider the viewing angles to the insides of your headlight housings. If the lenses on your housings are raked backwards at an angle, you'll likely want a deeper shroud to cover more of the projector body. If the headlight is only one flat surface on the front, a shroud that doesn't have as much depth from front to back will still work out well for you. The Ford Escape shown below is a good example of a flat faced headlight, where you can really only look into it from the front. There would be no opportunities to view the projector from the top or side, and therefore having a deep shroud isn't too important. On the other hand, the Hyundai's headlight lens is at an angle, therefore you may be able to see the projector from the top or side if it's not covered entirely with a deep shroud.</p>
                    <p><span class="text-weightBold">Shallow:</span> Gti-R, Ocular, E55-R</p>
                    <p><span class="text-weightBold">Medium:</span> Apollo, E46-R, E46-R Extended, Iris</p>
                    <p><span class="text-weightBold">Deep:</span> Bullet, G37-R, Gatling Gun, Gatling Gun 2.0, MC-R, Orbit, Panamera</p>

                    <hr class="aligncenter">

                    <p class="lead">4. Paint:</p>
                    <p>Are you going to paint the shrouds? If so, don't pay full-price for a set of our "premium" shrouds. Instead, pick up a pair of the "value" shrouds to save some extra money and ultimately end up with the same result. We have a discounted version available for most of the shrouds we carry.</p>
