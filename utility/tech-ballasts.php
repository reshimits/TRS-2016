                    <p class="lead">Cross compatibility between ballasts and bulbs:</p>
                </div>
                <div class="col-xs-12">
                    <p><span class="text-weightBold">Ballasts designed for use with D2S/D2R bulbs:</span> <br>
                    — The igniter is part of the ballast system.<br>
                    — AC85V stable current for bulbs containing mercury<br>
                    — The most commonly used kind for retrofits because the best performing projectors use D2S bulbs. </p>

                    <p><span class="text-weightBold">Ballasts designed for use with D1S/D1R: </span> <br>
                    — The igniter is part of the bulb, not part of the ballast system.<br>
                    — AC85V stable current for bulbs containing mercury<br>
                    — Becoming more common in OEM applications, but not widely used for retrofitting because projectors that use these bulbs lack in performance.</p>

                    <p><span class="text-weightBold">Ballasts designed for use with D3S/D3R: </span> <br>
                    — The igniter is part of the bulb, not part of the ballast system. <br>
                    — AC42V stable current for mercury-free HID bulbs<br>
                    — Most common in late-model American and European made applications from Audi, GM, and Ford.</p>

                    <p><span class="text-weightBold">Ballasts designed for use with D4S/D4R bulbs:</span> <br>
                    — The igniter is part of the ballast system.<br>
                    — AC42V stable current for mercury-free HID bulbs<br>
                    — Originated and still most common in Toyota/Lexus applications in their efforts to be eco friendly. Generally a bit more bright, but not quite as white!</p>

