                    <p><span class="text-weightBold">2. What wattage is right for you?</span> When it comes to ballasts, the wattage rating will determine the intensity of light produced by the bulb. The two most common options are 35w and 55w, but here and there, you'll see 70-100w ballasts too. Without exception, all vehicles with factory-equipped HID headlights run 35w ballasts. With the variety of choices here, what's right for you?</p>
                </div>
                <div class="col-xs-12 col-md-4">
                    <p><span class="text-weightBold">35w:</span> <br>
                    — Provides the best everyday and long term reliability.<br>
                    — Runs the bulbs at the coolest possible operating temperatures, aiding in the longevity of crucial reflective finishes inside the projectors.<br>
                    — Produces a light output that's still more than adequately bright, especially when coupled with a good set of projectors.<br>
                    — Bulbs will run at their rated Kelvin/color temperature.</p>
                </div>
                <div class="col-xs-12 col-md-4">
                    <p><span class="text-weightBold">55w:</span> <br>
                    — Provides approximately 25-30% more intensity in the light output.<br>
                    — Reduced reliability and lifespan of the ballast itself and the bulb.<br>
                    — Added heat can potentially reduce the reflectivity inside projectors after prolonged use, especially if the projector is small in size (making it less capable of absorbing and shedding the heat)<br>
                    — Washes out the color of the HID bulb (if that bulb is not specially made for 55w ballasts)</p>
                </div>
                <div class="col-xs-12 col-md-4">
                    <p><span class="text-weightBold">70-100w:</span> <br>
                    — Extremely bright, glaring output even if used in a projector based headlight.<br>
                    — Poor short term and long term reliability which also affects the bulbs.<br>
                    — High potential for over-heating and ruining crucial surfaces inside headlights/projectors.<br>
                    — Consumes a lot of power from the car's electrical system.</p>
