                    <p><span class="text-weightBold">1. What kind of projectors are you using?</span> The projectors ultimately determine the bulb size, which determines compatibility with ballasts. For example, projectors such as the FX-R Bi-xenon, TL Bi-xenon, or S2000 low beam all take D2S bulbs - meaning you'll need a ballast with an output for the D2 bulb base. Projectors like the Morimoto Mini H1 take a H1 HID bulb which will have AMP inputs, and therefore need ballasts with AMP outputs.</p>
