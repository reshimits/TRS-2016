                    <h3>Further Reading</h3>
                    <p class="lead">Ballasts start up in three phases:</p>
                </div>
                <div class="col-xs-12 col-md-4">
                    <p><span class="text-weightBold">1. Ignition:</span> <br>
                    The ballast sends a high voltage pulse through its output similar to a spark plug which travels across the electrodes in the HID bulb, ionizing the halide salts and xenon gas inside it's capsule to create light.</p>
                </div>
                <div class="col-xs-12 col-md-4">
                    <p><span class="text-weightBold">2. Initial phase/warm up:</span> <br>
                    The bulb is driven with a controlled overload from the ballast. Here, the bulb's capsule is still operating at high voltage and the temperature is rising quickly, vaporizing the halide salts and making the light output from the bulb spectrally complete - which fills out the beam pattern. The resistance between the electrodes also falls; the electronic ballast control gear registers this and automatically switches to continuous operation.</p>
                </div>
                <div class="col-xs-12 col-md-4">
                    <p><span class="text-weightBold">3. Continuous operation:</span> <br>
                    All metal salts are in the vapor phase, the arc has attained its stable shape and the lumen output from the bulb is maxed out. The ballast now supplies stable electrical current so the bulb won't/shouldn't flicker. In D1 and D2 systems, the stable operating voltage is AC85V, and AC42V in D3/D4 systems.</p>
                </div>
                </div>
            </div>
