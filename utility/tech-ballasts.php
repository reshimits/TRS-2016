                    <p><span class="text-weightBold">Is brighter always better? </span>Looking for a brighter output? One way to make your bulbs burn brighter would be to select 55w high output ballasts. All OEM applications use standard 35w units, but if you're a retrofitter looking to put the most possible light on the road, then these ballasts are for you. 55w bulbs are also available, and highly recommended for use with these powerful ballasts. You can use 55w ballasts with standard 35w bulbs, but the life of the bulb will be shortened a bit, and their color will Wash Out a bit. Again, OEM applications use 35w ballasts, and when coupled with a high performance projector, you'll really be putting plenty of light on the road even going that route. Some may argue that 55w ballasts pump up the foreground light so much that your eyes will adjust to such intense light so close, ultimately reducing long distance vision. In general, TRS advocates the use of 35w ballasts for most users and their applications.</p>

