                    <h3>Performance</h3>

                    <p><span class="text-weightBold">Light intensity:</span> It's pretty simple here. The lens is what actually projects the light forward and onto the ground. The more clear it is, the more light it's capable of shining on the road.</p>
                    <p><span class="text-weightBold">Beam width:</span> A projector's beam pattern will dim towards its outer edges naturally. Because a clear lens enhances intensity throughout the entire beam, the very edges of the output will be brighter, giving it the effect of increased width.</p>
                    <p><span class="text-weightBold">Beam uniformity:</span> Clear lenses help smooth out the beam by brightening up dark spots to help improve the overall distribution of light.</p>
