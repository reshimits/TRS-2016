                    <h3>Clear Lens Aesthetics</h3>

                    <p><span class="text-weightBold">Cutoff sharpness:</span> Beam pattern definition will be greatly enhanced with a razor sharp light cutoff line to separate the light from darkness.</p>
                    <p><span class="text-weightBold">Color flicker:</span> Compared to a Fresnel or translucent/foggy glass lens, an optically clear lens will add a band of colorful light across the top of the beam's cutoff line when set up correctly. This will ultimately enhance the aesthetic appeal of your output from an onlooker's perspective. The result is known as "color flicker" - this exotic effect occurs as this band of colorful light passes in and out of the field of vision of onlookers as you drive down a bumpy road or over the crest of a hill. Using a clear lens and a relatively low Kelvin bulb such as a 4300K or 5000K, you'll get the best of both worlds - great "flashy" looks coupled with maximum luminosity. From a distance, the headlights will flash through the colors appearing in your colorband (ie aqua/blue/purple) but standing next to the car at night, the headlights will appear to have a normal white output.</p>
                    <p>Often times, people think they can achieve the color flicker effect simply by using a high Kelvin bulb such as an 8000K or 10000K, but they're really just changing the color (and reducing the intensity) of the light within the beam and not adjusting the hues in the cutoff line itself. In comparison to the situation above, your headlights will appear ridiculously blue/purple from a distance, and you'll still look like an idiot from close up. The actual "flicker" will be reduced, as it's being drowned out by the color of the actual light being emitted.</p>
